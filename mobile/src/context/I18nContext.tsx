const I18nContext = createContext<I18nContextType | undefined>(undefined);

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_match, key) => String(params[key] ?? ""));
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>("en");
  const [ready, setReady] = useState(false);

  useEffect(() => {
    let mounted = true;

    const restoreLanguage = async () => {
      try {
        const stored = await SecureStore.getItemAsync(LANGUAGE_KEY);
        if (!mounted) return;
        if (stored === "en" || stored === "zh" || stored === "id") {
          setLanguageState(stored);
        }
      } finally {
        if (mounted) {
          setReady(true);
        }
      }
    };

    restoreLanguage();

    return () => {
      mounted = false;
    };
  }, []);

  const setLanguage = async (nextLanguage: Language) => {
    setLanguageState(nextLanguage);
    await SecureStore.setItemAsync(LANGUAGE_KEY, nextLanguage);
  };

  const value = useMemo<I18nContextType>(() => ({
    language,
    locale: localeByLanguage[language],
    ready,
    setLanguage,
    t: (key, params) => interpolate(translations[language][key] ?? translations.en[key], params),
    languageLabel: (value = language) => {
      if (value === "zh") return translations[language].language_chinese;
      if (value === "id") return translations[language].language_indonesian;
      return translations[language].language_english;
    },
  }), [language, ready]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}